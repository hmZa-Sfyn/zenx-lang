package main

import (
	"fmt"
	"strings"
)

// ─────────────────────────────────────────────────────────────────────────────
//  Emitter
// ─────────────────────────────────────────────────────────────────────────────

type Emitter struct {
	sb     strings.Builder
	indent int
}

func Emit(prog *Program) string {
	e := &Emitter{}
	e.emitProgram(prog)
	return e.sb.String()
}

func (e *Emitter) ln(f string, args ...any) {
	e.sb.WriteString(strings.Repeat("    ", e.indent))
	if len(args) > 0 { e.sb.WriteString(fmt.Sprintf(f, args...)) } else { e.sb.WriteString(f) }
	e.sb.WriteByte('\n')
}

func (e *Emitter) emitProgram(prog *Program) {
	e.ln("/* generated by zxc v0.4 — do not edit */")
	e.ln("#include <stdio.h>")
	e.ln("#include <stdlib.h>")
	e.ln("#include <string.h>")
	e.ln("#include <math.h>")
	e.ln("#include <stdbool.h>")
	e.ln("")

	// user C imports
	for _, imp := range prog.Imports {
		if !imp.IsStd && imp.Path != "" {
			e.ln("#include <%s>", imp.Path)
		}
	}

	// std module headers
	for _, h := range prog.StdHeaders() {
		// avoid duplicates already included above
		if h != "stdio.h" && h != "stdlib.h" && h != "string.h" && h != "math.h" {
			e.ln("#include <%s>", h)
		}
	}

	// helper: int_to_str used by std::conv
	needsIntToStr := false
	for _, imp := range prog.Imports {
		if imp.IsStd && imp.Module == "std::conv" { needsIntToStr = true }
	}
	if needsIntToStr {
		e.ln("")
		e.ln("/* std::conv helper */")
		e.ln("static const char* __zx_int_to_str(long long n) {")
		e.indent++
		e.ln("static char buf[64];")
		e.ln("snprintf(buf, sizeof(buf), \"%%lld\", n);")
		e.ln("return buf;")
		e.indent--
		e.ln("}")
	}
	e.ln("")

	// struct typedefs
	for _, s := range prog.Structs { e.emitStruct(s) }

	// extern prototypes (skip known C stdlib)
	for _, ext := range prog.Externs {
		if !knownCFuncs[ext.Name] { e.emitExternProto(ext) }
	}

	// std module fn prototypes (just type-checking aids; C already has them)
	// no emission needed — headers take care of it

	// forward-declare user fns & methods
	for _, stmt := range prog.TopStmts {
		if fn, ok := stmt.(*FnDecl); ok && fn.Name != "main" { e.emitFnProto(fn) }
	}
	for _, m := range prog.Methods { e.emitMethodProto(m) }
	e.ln("")

	// function bodies
	var mainStmts []Node
	var fnDecls []*FnDecl
	hasUserMain := false
	for _, stmt := range prog.TopStmts {
		if fn, ok := stmt.(*FnDecl); ok {
			fnDecls = append(fnDecls, fn)
			if fn.Name == "main" { hasUserMain = true }
		} else {
			mainStmts = append(mainStmts, stmt)
		}
	}
	for _, fn := range fnDecls { e.emitFnFull(fn); e.ln("") }
	for _, m := range prog.Methods { e.emitMethodFull(m); e.ln("") }

	// main() wrapping top-level stmts
	if !hasUserMain {
		e.ln("int main(int argc, char **argv) {")
		e.indent++
		for _, s := range mainStmts { e.emitStmt(s) }
		e.ln("return 0;")
		e.indent--
		e.ln("}")
	}
}

// ── C types ───────────────────────────────────────────────────────────────────

func cType(t *ZXType) string {
	if t == nil { return "long long" }
	switch t.Kind {
	case TyInt:   return "long long"
	case TyFloat: return "double"
	case TyBool:  return "int"
	case TyStr:   return "const char*"
	case TyChar:  return "char"
	case TyVoid:  return "void"
	case TyAny:   return "long long"
	case TyRef:
		if t.Elem != nil { return cType(t.Elem) + "*" }
		return "void*"
	case TyArray:
		if t.Elem != nil { return cType(t.Elem) }
		return "void*"
	case TySlice:
		if t.Elem != nil { return cType(t.Elem) + "*" }
		return "void*"
	case TyStruct: return t.Name
	default:       return "long long"
	}
}

func cTypeDecl(t *ZXType, name string) string {
	if t != nil && (t.Kind == TyArray) {
		sz := ""; if t.ArrSize > 0 { sz = fmt.Sprintf("%d", t.ArrSize) }
		return fmt.Sprintf("%s %s[%s]", cType(t.Elem), name, sz)
	}
	return fmt.Sprintf("%s %s", cType(t), name)
}

// ── Struct ────────────────────────────────────────────────────────────────────

func (e *Emitter) emitStruct(s *StructDecl) {
	e.ln("typedef struct %s {", s.Name)
	e.indent++
	for _, f := range s.Fields {
		t := f.Type; if t == nil { t = TypAny }
		e.ln("%s;", cTypeDecl(t, f.Name))
	}
	e.indent--
	e.ln("} %s;", s.Name)
	e.ln("")
}

func (e *Emitter) emitExternProto(ext *ExternDecl) {
	e.ln("extern %s %s(%s);", cType(ext.RetType), ext.Name, buildParamStr(ext.Params, ext.Variadic))
}

func (e *Emitter) emitFnProto(fn *FnDecl) {
	e.ln("%s %s(%s);", cType(fn.RetType), fn.Name, buildParamStr(fn.Params, fn.Variadic))
}

func (e *Emitter) emitFnFull(fn *FnDecl) {
	sig := ""
	if fn.Name == "main" { sig = "int main(int argc, char **argv)" } else { sig = fmt.Sprintf("%s %s(%s)", cType(fn.RetType), fn.Name, buildParamStr(fn.Params, fn.Variadic)) }
	e.ln("%s {", sig)
	e.indent++
	for _, s := range fn.Body.Stmts { e.emitStmt(s) }
	if fn.Name == "main" { e.ln("return 0;") }
	e.indent--
	e.ln("}")
}

func (e *Emitter) emitMethodProto(m *MethodDecl) {
	e.ln("%s %s(%s);", cType(m.RetType), m.CName(), methodParamStr(m))
}

func (e *Emitter) emitMethodFull(m *MethodDecl) {
	e.ln("%s %s(%s) {", cType(m.RetType), m.CName(), methodParamStr(m))
	e.indent++
	for _, s := range m.Body.Stmts { e.emitStmt(s) }
	e.indent--
	e.ln("}")
}

func methodParamStr(m *MethodDecl) string {
	recvTyp := m.RecvType; if m.RecvRef { recvTyp = m.RecvType + "*" }
	parts := []string{recvTyp + " " + m.RecvName}
	for _, p := range m.Params { parts = append(parts, cTypeDecl(p.Type, p.Name)) }
	if m.Variadic { parts = append(parts, "...") }
	return strings.Join(parts, ", ")
}

func buildParamStr(params []Param, variadic bool) string {
	if len(params) == 0 { if variadic { return "..." }; return "void" }
	parts := make([]string, len(params))
	for i, p := range params { parts[i] = cTypeDecl(p.Type, p.Name) }
	if variadic { parts = append(parts, "...") }
	return strings.Join(parts, ", ")
}

// ── Statements ────────────────────────────────────────────────────────────────

func (e *Emitter) emitStmt(n Node) {
	if n == nil { return }
	switch s := n.(type) {
	case *VarDecl:      e.emitVarDecl(s)
	case *ReturnStmt:   e.emitReturn(s)
	case *IfStmt:       e.emitIf(s)
	case *UnlessStmt:
		e.ln("if (!(%s)) {", e.emitExpr(s.Cond))
		e.indent++; for _, st := range s.Body.Stmts { e.emitStmt(st) }; e.indent--
		if s.Else != nil { e.ln("} else {"); e.indent++; for _, st := range s.Else.Stmts { e.emitStmt(st) }; e.indent-- }
		e.ln("}")
	case *WhileStmt:    e.emitWhile(s)
	case *UntilStmt:
		e.ln("while (!(%s)) {", e.emitExpr(s.Cond))
		e.indent++; for _, st := range s.Body.Stmts { e.emitStmt(st) }; e.indent--; e.ln("}")
	case *ForRangeStmt: e.emitForRange(s)
	case *AssignStmt:   e.ln("%s %s %s;", e.emitExpr(s.LHS), s.Op, e.emitExpr(s.Value))
	case *ExprStmt:
		ex := e.emitExpr(s.Expr); if ex != "0" && ex != "" { e.ln("%s;", ex) }
	case *PrintStmt:    e.emitPrint(s)
	case *ExitStmt:     e.ln("exit((int)(%s));", e.emitExpr(s.Code))
	case *BreakStmt:    e.ln("break;")
	case *ContinueStmt: e.ln("continue;")
	case *FnDecl:       e.emitFnFull(s)
	case *Block:
		e.ln("{"); e.indent++; for _, st := range s.Stmts { e.emitStmt(st) }; e.indent--; e.ln("}")
	case *PipeExpr:
		result := e.emitExpr(s); e.ln("%s;", result)
	}
}

func (e *Emitter) emitVarDecl(v *VarDecl) {
	typ := v.ResolvedType; if typ == nil { typ = v.VarType }; if typ == nil { typ = TypAny }
	prefix := ""; if v.IsConst { prefix = "const " }
	if v.Init != nil {
		e.ln("%s%s = %s;", prefix, cTypeDecl(typ, v.Name), e.emitExpr(v.Init))
	} else {
		e.ln("%s%s;", prefix, cTypeDecl(typ, v.Name))
	}
}

func (e *Emitter) emitReturn(r *ReturnStmt) {
	if r.Value == nil { e.ln("return;") } else { e.ln("return %s;", e.emitExpr(r.Value)) }
}

func (e *Emitter) emitIf(s *IfStmt) {
	e.ln("if (%s) {", e.emitExpr(s.Cond))
	e.indent++; for _, st := range s.Then.Stmts { e.emitStmt(st) }; e.indent--
	for _, el := range s.Elifs {
		e.ln("} else if (%s) {", e.emitExpr(el.Cond))
		e.indent++; for _, st := range el.Body.Stmts { e.emitStmt(st) }; e.indent--
	}
	if s.Else != nil { e.ln("} else {"); e.indent++; for _, st := range s.Else.Stmts { e.emitStmt(st) }; e.indent-- }
	e.ln("}")
}

func (e *Emitter) emitWhile(s *WhileStmt) {
	e.ln("while (%s) {", e.emitExpr(s.Cond))
	e.indent++; for _, st := range s.Body.Stmts { e.emitStmt(st) }; e.indent--; e.ln("}")
}

func (e *Emitter) emitForRange(s *ForRangeStmt) {
	from := e.emitExpr(s.From); to := e.emitExpr(s.To)
	e.ln("for (long long %s = %s; %s < %s; %s++) {", s.Var, from, s.Var, to, s.Var)
	e.indent++; for _, st := range s.Body.Stmts { e.emitStmt(st) }; e.indent--; e.ln("}")
}

func (e *Emitter) emitPrint(s *PrintStmt) {
	dest := "stdout"; if s.ToStderr { dest = "stderr" }
	if len(s.Args) == 0 {
		if s.Newline { e.ln(`fprintf(%s, "\n");`, dest) }; return
	}
	var fmts, args []string
	for _, a := range s.Args { fmts = append(fmts, fmtFor(exprType(a))); args = append(args, e.emitExpr(a)) }
	fmtStr := strings.Join(fmts, " "); if s.Newline { fmtStr += "\\n" }
	e.ln(`fprintf(%s, "%s", %s);`, dest, fmtStr, strings.Join(args, ", "))
}

// ── Expressions ───────────────────────────────────────────────────────────────

func (e *Emitter) emitExpr(n Node) string {
	if n == nil { return "0" }
	switch ex := n.(type) {
	case *IntLit:   return fmt.Sprintf("%dLL", ex.Val)
	case *FloatLit:
		s := fmt.Sprintf("%g", ex.Val)
		if !strings.Contains(s, ".") && !strings.Contains(s, "e") { s += ".0" }
		return s
	case *BoolLit:  if ex.Val { return "1" }; return "0"
	case *StrLit:   return cEscapeString(ex.Val)
	case *NilLit:   return "NULL"
	case *Ident:    return ex.Name

	case *BinExpr:
		return fmt.Sprintf("(%s %s %s)", e.emitExpr(ex.LHS), ex.Op, e.emitExpr(ex.RHS))

	case *UnaryExpr:
		return fmt.Sprintf("(%s%s)", ex.Op, e.emitExpr(ex.Operand))

	case *CallExpr:
		fn := e.emitExpr(ex.Func)
		var argStrs []string
		for _, a := range ex.Args { argStrs = append(argStrs, e.emitExpr(a)) }
		// check if it's a std fn with different C name
		if id, ok := ex.Func.(*Ident); ok {
			// no special rename needed — std fns use C names directly
			_ = id
		}
		return fmt.Sprintf("%s(%s)", fn, strings.Join(argStrs, ", "))

	case *BuiltinExpr:
		return e.emitBuiltin(ex)

	case *MethodCallExpr:
		recvStr := e.emitExpr(ex.Recv)
		recvType := exprType(ex.Recv)
		structName := ""
		if recvType != nil {
			if recvType.Kind == TyStruct { structName = recvType.Name }
			if recvType.Kind == TyRef && recvType.Elem != nil && recvType.Elem.Kind == TyStruct { structName = recvType.Elem.Name }
		}
		var argStrs []string; argStrs = append(argStrs, recvStr)
		for _, a := range ex.Args { argStrs = append(argStrs, e.emitExpr(a)) }
		if structName != "" { return fmt.Sprintf("%s_%s(%s)", structName, ex.Method, strings.Join(argStrs, ", ")) }
		return fmt.Sprintf("%s(%s)", ex.Method, strings.Join(argStrs, ", "))

	case *PipeExpr:
		return e.emitPipeExpr(ex)

	case *IndexExpr:
		return fmt.Sprintf("%s[%s]", e.emitExpr(ex.Obj), e.emitExpr(ex.Idx))

	case *FieldExpr:
		obj := e.emitExpr(ex.Obj)
		objType := exprType(ex.Obj)
		// if obj is a ref, always emit ->
		if objType != nil && objType.Kind == TyRef { return fmt.Sprintf("%s->%s", obj, ex.Field) }
		return fmt.Sprintf("%s.%s", obj, ex.Field)

	case *CastExpr:
		return fmt.Sprintf("((%s)(%s))", cType(ex.ToType), e.emitExpr(ex.Operand))

	case *AddrExpr:
		if ex.Deref { return fmt.Sprintf("(*%s)", e.emitExpr(ex.Operand)) }
		return fmt.Sprintf("(&%s)", e.emitExpr(ex.Operand))

	case *StructInit:
		var parts []string
		for _, fi := range ex.Fields { parts = append(parts, fmt.Sprintf(".%s = %s", fi.Name, e.emitExpr(fi.Value))) }
		if ex.HeapAlloc {
			return fmt.Sprintf("((%s*)memcpy(malloc(sizeof(%s)), &(%s){%s}, sizeof(%s)))",
				ex.Name, ex.Name, ex.Name, strings.Join(parts, ", "), ex.Name)
		}
		return fmt.Sprintf("(%s){%s}", ex.Name, strings.Join(parts, ", "))

	case *ArrayLit:
		var parts []string
		for _, el := range ex.Elems { parts = append(parts, e.emitExpr(el)) }
		return fmt.Sprintf("{%s}", strings.Join(parts, ", "))

	case *SizeofExpr:
		return fmt.Sprintf("(long long)sizeof(%s)", cType(ex.Of))

	default: return "0"
	}
}

// ── Builtin emission ──────────────────────────────────────────────────────────

func (e *Emitter) emitBuiltin(b *BuiltinExpr) string {
	bd := LookupBuiltin(b.Name)
	if bd == nil {
		// fallthrough: len, push, pop
		switch b.Name {
		case "len":
			if len(b.Args) == 1 { return fmt.Sprintf("(long long)strlen((const char*)(%s))", e.emitExpr(b.Args[0])) }
		case "push":
			// not implemented yet, just emit comment
			return "/* push: use raw array index */"
		case "pop":
			return "/* pop: use raw array index */"
		}
		var argStrs []string
		for _, a := range b.Args { argStrs = append(argStrs, e.emitExpr(a)) }
		return fmt.Sprintf("%s(%s)", b.Name, strings.Join(argStrs, ", "))
	}
	if bd.DupArgs && len(b.Args) == 2 {
		a0 := e.emitExpr(b.Args[0]); a1 := e.emitExpr(b.Args[1])
		return fmt.Sprintf(bd.Emit, a0, a1, a0, a1)
	}
	if len(b.Args) == 1 { return fmt.Sprintf(bd.Emit, e.emitExpr(b.Args[0])) }
	if len(b.Args) == 0 { return bd.Emit }
	var argStrs []string
	for _, a := range b.Args { argStrs = append(argStrs, e.emitExpr(a)) }
	return fmt.Sprintf(bd.Emit, strings.Join(argStrs, ", "))
}

// ── Pipe emission: a |> b |> c  →  c(b(a)) ───────────────────────────────────

func (e *Emitter) emitPipeExpr(p *PipeExpr) string {
	if len(p.Steps) == 0 { return "0" }
	result := e.emitExpr(p.Steps[0])
	for _, step := range p.Steps[1:] {
		fnName := e.emitExpr(step)
		result = fmt.Sprintf("%s(%s)", fnName, result)
	}
	return result
}

// ── helpers ───────────────────────────────────────────────────────────────────

func fmtFor(t *ZXType) string {
	if t == nil { return "%s" }
	switch t.Kind {
	case TyInt:   return "%lld"
	case TyFloat: return "%g"
	case TyBool:  return "%d"
	case TyStr:   return "%s"
	case TyChar:  return "%c"
	case TyRef:   return "%p"
	default:      return "%s"
	}
}

func exprType(n Node) *ZXType {
	switch e := n.(type) {
	case *IntLit:         return TypInt
	case *FloatLit:       return TypFloat
	case *BoolLit:        return TypBool
	case *StrLit:         return TypStr
	case *Ident:          return e.Typ
	case *BinExpr:        return e.Typ
	case *UnaryExpr:      return e.Typ
	case *CallExpr:       return e.Typ
	case *MethodCallExpr: return e.Typ
	case *BuiltinExpr:    return e.Typ
	case *CastExpr:       return e.Typ
	case *AddrExpr:       return e.Typ
	case *FieldExpr:      return e.Typ
	case *IndexExpr:      return e.Typ
	case *ArrayLit:       return e.Typ
	case *PipeExpr:       return e.Typ
	case *SizeofExpr:     return TypInt
	default:              return TypAny
	}
}

func cEscapeString(s string) string {
	var sb strings.Builder; sb.WriteByte('"')
	for _, r := range s {
		switch r {
		case '"':  sb.WriteString(`\"`)
		case '\\': sb.WriteString(`\\`)
		case '\n': sb.WriteString(`\n`)
		case '\t': sb.WriteString(`\t`)
		case '\r': sb.WriteString(`\r`)
		case 0:    sb.WriteString(`\0`)
		default:   sb.WriteRune(r)
		}
	}
	sb.WriteByte('"'); return sb.String()
}
