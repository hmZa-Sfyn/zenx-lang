package main

import (
	"fmt"
	"strings"
)

type Emitter struct {
	sb         strings.Builder
	globalBuf  strings.Builder
	indent     int
	deferBuf   []string
	tmpCounter int
	prog       *Program
	// lambda counter for unique C function names
	lambdaCounter int
	// accumulated lambda bodies (emitted before main)
	lambdaDecls  strings.Builder
	lambdaBodies strings.Builder
}

func Emit(prog *Program) string {
	e := &Emitter{prog: prog}
	e.emitProgram(prog)
	return e.sb.String()
}

func (e *Emitter) tmp() string {
	e.tmpCounter++
	return fmt.Sprintf("__zx_tmp%d", e.tmpCounter)
}

func (e *Emitter) lambdaName() string {
	e.lambdaCounter++
	return fmt.Sprintf("__zx_lambda%d", e.lambdaCounter)
}

func (e *Emitter) ln(f string, args ...any) {
	e.sb.WriteString(strings.Repeat("    ", e.indent))
	if len(args) > 0 {
		e.sb.WriteString(fmt.Sprintf(f, args...))
	} else {
		e.sb.WriteString(f)
	}
	e.sb.WriteByte('\n')
}

func (e *Emitter) raw(s string) { e.sb.WriteString(s); e.sb.WriteByte('\n') }

func (e *Emitter) gln(f string, args ...any) {
	if len(args) > 0 {
		e.globalBuf.WriteString(fmt.Sprintf(f, args...))
	} else {
		e.globalBuf.WriteString(f)
	}
	e.globalBuf.WriteByte('\n')
}

func (e *Emitter) lln(f string, args ...any) {
	if len(args) > 0 {
		e.lambdaBodies.WriteString(fmt.Sprintf(f, args...))
	} else {
		e.lambdaBodies.WriteString(f)
	}
	e.lambdaBodies.WriteByte('\n')
}

// ─────────────────────────────────────────────────────────────────────────────
//  Program emission
// ─────────────────────────────────────────────────────────────────────────────

func (e *Emitter) emitProgram(prog *Program) {
	e.ln("/* generated by zxc v0.3.2 — do not edit */")
	e.ln("#include <stdio.h>")
	e.ln("#include <stdlib.h>")
	e.ln("#include <string.h>")
	e.ln("#include <math.h>")
	e.ln("#include <stdbool.h>")
	e.ln("#include <stdint.h>")
	e.ln("#include <errno.h>")
	e.ln("#include <assert.h>")
	e.ln("")

	for _, imp := range prog.Imports {
		if !imp.IsStd && imp.Path != "" {
			e.ln("#include <%s>", imp.Path)
		}
	}
	for _, h := range prog.StdHeaders() {
		e.ln("#include <%s>", h)
	}
	e.ln("")

	for _, h := range prog.StdHelpers() {
		e.raw(h)
	}
	if len(prog.StdHelpers()) > 0 {
		e.ln("")
	}

	// ── built-in helpers ──────────────────────────────────────────────────────

	e.ln("/* built-in input() helper */")
	e.ln("static char __zx_input_buf[4096];")
	e.ln("static const char* __zx_input(const char* prompt) {")
	e.indent++
	e.ln("if (prompt) { fputs(prompt, stdout); fflush(stdout); }")
	e.ln("if (!fgets(__zx_input_buf, sizeof(__zx_input_buf), stdin)) return \"\";")
	e.ln("size_t l = strlen(__zx_input_buf);")
	e.ln("if (l > 0 && __zx_input_buf[l-1] == '\\n') __zx_input_buf[l-1] = '\\0';")
	e.ln("return __zx_input_buf;")
	e.indent--
	e.ln("}")
	e.ln("")

	e.ln("/* built-in cmd!() capture helper */")
	e.ln("static char __zx_cmd_buf[65536];")
	e.ln("static const char* __zx_capture_cmd(const char* cmd) {")
	e.indent++
	e.ln("FILE* p = popen(cmd, \"r\");")
	e.ln("if (!p) { __zx_cmd_buf[0]='\\0'; return __zx_cmd_buf; }")
	e.ln("size_t total = 0; char tmp[1024];")
	e.ln("while (fgets(tmp, sizeof(tmp), p) && total + strlen(tmp) < sizeof(__zx_cmd_buf)-1) {")
	e.indent++
	e.ln("memcpy(__zx_cmd_buf + total, tmp, strlen(tmp)); total += strlen(tmp);")
	e.indent--
	e.ln("}")
	e.ln("__zx_cmd_buf[total] = '\\0'; pclose(p); return __zx_cmd_buf;")
	e.indent--
	e.ln("}")
	e.ln("")

	e.ln("/* built-in template string buffer */")
	e.ln("static char __zx_fmt_buf[65536];")
	e.ln("")

	// __zx_len: safe len() for any-typed values.
	// If the long long looks like a valid string pointer (> 0xFFFF), call strlen.
	// Otherwise return 0 — the value is an integer, not a string.
	e.ln("/* safe len() for any-typed values */")
	e.ln("static long long __zx_len(long long __v) {")
	e.indent++
	e.ln("if (__v > 0xFFFF) return (long long)strlen((const char*)__v);")
	e.ln("return 0LL;")
	e.indent--
	e.ln("}")
	e.ln("")

	// __zx_any_str: extract string from an any (long long) value.
	// Used when printing any-typed values as strings.
	e.ln("/* extract string representation from any-typed value */")
	e.ln("static char __zx_any_buf[256];")
	e.ln("static const char* __zx_any_str(long long __v) {")
	e.indent++
	e.ln("if (__v > 0xFFFF) return (const char*)__v; /* looks like a string ptr */")
	e.ln("snprintf(__zx_any_buf, sizeof(__zx_any_buf), \"%%lld\", __v);")
	e.ln("return __zx_any_buf;")
	e.indent--
	e.ln("}")
	e.ln("")

	// ── structs ───────────────────────────────────────────────────────────────

	for _, s := range prog.Structs {
		e.emitStruct(s)
	}

	// ── externs ───────────────────────────────────────────────────────────────

	for _, ext := range prog.Externs {
		if !knownCFuncs[ext.Name] {
			e.ln("extern %s %s(%s);", cType(ext.RetType), ext.Name, buildParamStr(ext.Params, ext.Variadic))
		}
	}

	// ── macro forward declarations (non-chain) ────────────────────────────────

	for _, mc := range prog.Macros {
		if isChainMacro(mc) {
			continue
		}
		e.ln("%s %s(%s);", cType(mc.RetType), macroFnName(mc.Name), macroParamStr(mc))
		for _, alias := range mc.Aliases {
			e.ln("#define %s %s", macroFnName(alias), macroFnName(mc.Name))
		}
	}

	// ── top-level fn forward declarations ────────────────────────────────────

	for _, stmt := range prog.TopStmts {
		if fn, ok := stmt.(*FnDecl); ok && fn.Name != "main" {
			cname := fn.Name
			if fn.CName != "" {
				cname = fn.CName
			}
			e.ln("%s %s(%s);", cType(fn.RetType), safeFnName(cname), buildParamStr(fn.Params, fn.Variadic))
		}
	}
	for _, m := range prog.Methods {
		e.ln("%s %s(%s);", cType(m.RetType), m.CName(), methodParamStr(m))
	}
	e.ln("")

	// ── macro bodies (non-chain) ──────────────────────────────────────────────

	for _, mc := range prog.Macros {
		if isChainMacro(mc) {
			continue
		}
		e.emitMacroFull(mc)
		e.ln("")
	}

	// ── global 'our' variables ────────────────────────────────────────────────

	for _, vd := range prog.GlobalVars {
		declared := vd.ResolvedType
		if declared == nil {
			declared = vd.VarType
		}

		var ct string
		if declared != nil && declared.Kind != TyAny && declared.Kind != TyUnknown {
			if declared.Kind == TyArray {
				sz := ""
				if declared.ArrSize > 0 {
					sz = fmt.Sprintf("%d", declared.ArrSize)
				}
				prefix := ""
				if vd.IsConst {
					prefix = "const "
				}
				if vd.Init != nil {
					e.ln("%s%s %s[%s] = %s;", prefix, cType(declared.Elem), vd.Name, sz, e.emitExpr(vd.Init))
				} else {
					e.ln("%s%s %s[%s];", prefix, cType(declared.Elem), vd.Name, sz)
				}
				continue
			}
			ct = cType(declared)
		} else if vd.Init != nil {
			ct = cTypeFromExpr(declared, vd.Init)
		} else {
			ct = "void*"
		}

		prefix := ""
		if vd.IsConst {
			prefix = "const "
		}
		if vd.Init != nil {
			e.ln("%s%s %s = %s;", prefix, ct, vd.Name, e.emitExpr(vd.Init))
		} else {
			e.ln("%s%s %s = %s;", prefix, ct, vd.Name, zeroValueFor(ct))
		}
	}
	if len(prog.GlobalVars) > 0 {
		e.ln("")
	}

	// ── mod-level consts ──────────────────────────────────────────────────────

	for _, mb := range prog.ModBlocks {
		e.emitModConsts(mb)
	}

	// ── mod property forward declarations ────────────────────────────────────
	// Must come before fwdDeclModFns so fn bodies can call getters.

	for _, mb := range prog.ModBlocks {
		e.fwdDeclModProperties(mb)
	}
	e.ln("")

	// ── mod fn forward declarations ───────────────────────────────────────────

	for _, mb := range prog.ModBlocks {
		e.fwdDeclModFns(mb)
	}

	// ── mod property implementations (backing vars + get/set) ─────────────────

	for _, mb := range prog.ModBlocks {
		e.emitModProperties(mb)
	}

	// ── mod fn bodies ─────────────────────────────────────────────────────────

	for _, mb := range prog.ModBlocks {
		e.emitModFns(mb)
	}

	// ── lambda bodies ─────────────────────────────────────────────────────────

	if e.lambdaBodies.Len() > 0 {
		e.sb.WriteString(e.lambdaBodies.String())
		e.ln("")
	}

	// ── top-level fns + methods ───────────────────────────────────────────────

	var mainStmts []Node
	var fnDecls []*FnDecl
	hasUserMain := false
	for _, stmt := range prog.TopStmts {
		if fn, ok := stmt.(*FnDecl); ok {
			fnDecls = append(fnDecls, fn)
			if fn.Name == "main" {
				hasUserMain = true
			}
		} else {
			mainStmts = append(mainStmts, stmt)
		}
	}
	for _, fn := range fnDecls {
		e.emitFnFull(fn)
		e.ln("")
	}
	for _, m := range prog.Methods {
		e.emitMethodFull(m)
		e.ln("")
	}

	// ── synthetic main ────────────────────────────────────────────────────────

	if !hasUserMain {
		e.ln("int main(int argc, char **argv) {")
		e.indent++
		// call mod init functions before user code
		for _, mb := range prog.ModBlocks {
			e.emitModInitCall(mb)
		}
		for _, s := range mainStmts {
			e.emitStmt(s)
		}
		e.ln("return 0;")
		e.indent--
		e.ln("}")
	}
}

// ─────────────────────────────────────────────────────────────────────────────
//  Mod constants
// ─────────────────────────────────────────────────────────────────────────────

func (e *Emitter) emitModConsts(mb *ModBlock) {
	for _, vd := range mb.Consts {
		declared := vd.ResolvedType
		if declared == nil {
			declared = vd.VarType
		}
		cname := modCFnName(mb.Path, vd.Name)

		var ct string
		if declared != nil && declared.Kind != TyAny && declared.Kind != TyUnknown {
			ct = cType(declared)
		} else if vd.Init != nil {
			ct = cTypeFromExpr(declared, vd.Init)
		} else {
			ct = "long long"
		}

		if vd.Init != nil {
			e.ln("static const %s %s = %s; /* mod %s const */",
				ct, cname, e.emitExpr(vd.Init), mb.Path)
		} else {
			e.ln("static %s %s = %s; /* mod %s const */",
				ct, cname, zeroValueFor(ct), mb.Path)
		}
	}
	for _, nested := range mb.Mods {
		e.emitModConsts(nested)
	}
}

func (e *Emitter) emitModInitCall(mb *ModBlock) {
	if mb.Init != nil {
		e.ln("%s(); /* mod %s init */", modCFnName(mb.Path, mb.Init.Name), mb.Path)
	}
	for _, nested := range mb.Mods {
		e.emitModInitCall(nested)
	}
}

// ─────────────────────────────────────────────────────────────────────────────
//  Mod properties
//
//  Each property compiles to:
//    static <type> ModPath__propName;            ← backing variable
//    static <type> ModPath__propName__get(void)  ← getter
//    static void   ModPath__propName__set(<type> value) ← setter
//
//  Read syntax:   MyMod::count      → MyMod__count__get()
//  Write syntax:  MyMod::count = 5  → MyMod__count__set(5)
//  Compound:      MyMod::count += 1 → MyMod__count__set(MyMod__count__get() + 1)
// ─────────────────────────────────────────────────────────────────────────────

func propBackingName(modPath, propName string) string {
	return modPathToC(modPath) + "__" + propName
}
func propGetterName(modPath, propName string) string {
	return propBackingName(modPath, propName) + "__get"
}
func propSetterName(modPath, propName string) string {
	return propBackingName(modPath, propName) + "__set"
}

// fwdDeclModProperties emits forward declarations for all property
// getter/setter functions so mod fn bodies can reference them.
func (e *Emitter) fwdDeclModProperties(mb *ModBlock) {
	for _, prop := range mb.Properties {
		typ := prop.Type
		if typ == nil {
			typ = TypAny
		}
		ct := cType(typ)
		getter := propGetterName(mb.Path, prop.Name)
		setter := propSetterName(mb.Path, prop.Name)
		param := prop.SetParam
		if param == "" {
			param = "value"
		}
		if prop.HasGet {
			e.ln("static %s %s(void);", ct, getter)
		}
		if prop.HasSet {
			e.ln("static void %s(%s %s);", setter, ct, param)
		}
	}
	for _, nested := range mb.Mods {
		e.fwdDeclModProperties(nested)
	}
}

// emitModProperties emits backing vars + getter/setter bodies for all
// properties in a mod block (and nested mods).
func (e *Emitter) emitModProperties(mb *ModBlock) {
	for _, prop := range mb.Properties {
		e.emitOneProperty(mb.Path, prop)
	}
	for _, nested := range mb.Mods {
		e.emitModProperties(nested)
	}
}

func (e *Emitter) emitOneProperty(modPath string, prop *ModProperty) {
	typ := prop.Type
	if typ == nil {
		typ = TypAny
	}
	backing := propBackingName(modPath, prop.Name)
	getter := propGetterName(modPath, prop.Name)
	setter := propSetterName(modPath, prop.Name)
	ct := cType(typ)
	param := prop.SetParam
	if param == "" {
		param = "value"
	}

	e.ln("/* mod %s property %s %s */", modPath, prop.Name, typ)

	// Backing variable
	if prop.Init != nil {
		e.ln("static %s = %s;", cTypeDecl(typ, backing), e.emitExpr(prop.Init))
	} else {
		e.ln("static %s;", cTypeDecl(typ, backing))
	}

	// Getter
	if prop.HasGet {
		e.ln("static %s %s(void) {", ct, getter)
		e.indent++
		if prop.GetBody != nil {
			// user-supplied body — expose backing field by its C name
			e.emitBlockStmtsWithPropBacking(prop.GetBody, backing)
		} else {
			e.ln("return %s;", backing)
		}
		e.indent--
		e.ln("}")
	}

	// Setter
	if prop.HasSet {
		e.ln("static void %s(%s %s) {", setter, ct, param)
		e.indent++
		if prop.SetBody != nil {
			e.emitBlockStmtsWithPropBacking(prop.SetBody, backing)
		} else {
			e.ln("%s = %s;", backing, param)
		}
		e.indent--
		e.ln("}")
	}

	e.ln("")
}

// emitBlockStmtsWithPropBacking emits a block where any identifier starting
// with "__" followed by a bare name is replaced with the actual C backing
// field name.  This lets users write:
//
//	get { return __count; }   →   return Counter__count;
//	set(v) { __count = v; }   →   Counter__count = v;
func (e *Emitter) emitBlockStmtsWithPropBacking(b *Block, backingName string) {
	if b == nil {
		return
	}
	// We emit normally — the typechecker already scoped the backing field
	// name into the body scope.  The raw identifier in user code is the C
	// backing name (e.g. "Counter__count"), so emitExpr just emits it as-is.
	e.emitBlockStmts(b)
}

// emitModPropGet emits the C expression for reading a mod property.
func (e *Emitter) emitModPropGet(ex *ModPropGetExpr) string {
	return fmt.Sprintf("%s()", propGetterName(ex.Mod, ex.Prop))
}

// emitModPropSet emits the C statement for writing a mod property.
func (e *Emitter) emitModPropSet(s *ModPropSetStmt) {
	setter := propSetterName(s.Mod, s.Prop)
	getter := propGetterName(s.Mod, s.Prop)
	val := e.emitExpr(s.Value)

	if s.Op == "=" {
		e.ln("%s(%s);", setter, val)
	} else {
		// compound: += -= *= /= %=
		// Expand:  setter(getter() op val)
		binOp := strings.TrimSuffix(s.Op, "=")
		e.ln("%s(%s() %s %s);", setter, getter, binOp, val)
	}
}

// ─────────────────────────────────────────────────────────────────────────────
//  Struct emission
// ─────────────────────────────────────────────────────────────────────────────

func (e *Emitter) emitStruct(s *StructDecl) {
	if s.Doc != "" {
		e.ln("/* %s */", s.Doc)
	}
	e.ln("typedef struct %s {", s.Name)
	e.indent++
	if s.Base != "" {
		e.ln("%s __base; /* inherits from %s */", s.Base, s.Base)
	}
	for _, f := range s.Fields {
		t := f.Type
		if t == nil || t.Kind == TyAny || t.Kind == TyUnknown {
			// any-typed struct fields → void* so they can hold any C value.
			e.ln("void* %s;", f.Name)
		} else {
			e.ln("%s;", cTypeDecl(t, f.Name))
		}
	}
	e.indent--
	e.ln("} %s;", s.Name)
	e.ln("")
}

// ─────────────────────────────────────────────────────────────────────────────
//  C name helpers
// ─────────────────────────────────────────────────────────────────────────────

var cReservedFnNames = map[string]bool{
	"double": true, "float": true, "int": true, "char": true,
	"long": true, "short": true, "unsigned": true, "signed": true,
	"void": true, "struct": true, "union": true, "enum": true,
	"static": true, "extern": true, "const": true, "inline": true,
	"register": true, "auto": true, "volatile": true, "typedef": true,
	"return": true, "if": true, "else": true, "while": true,
	"for": true, "do": true, "switch": true, "case": true,
	"break": true, "continue": true, "goto": true, "default": true,
	"sizeof": true, "bool": true, "string": true,
}

func safeFnName(name string) string {
	if cReservedFnNames[name] {
		return "__zx_" + name
	}
	return name
}

// modCFnName: "Outer::Inner" + "fn" → "Outer_Inner_fn"
func modCFnName(modPath, fnName string) string {
	return strings.ReplaceAll(modPath, "::", "_") + "_" + fnName
}

// ─────────────────────────────────────────────────────────────────────────────
//  Function / method emission
// ─────────────────────────────────────────────────────────────────────────────

func (e *Emitter) emitFnFull(fn *FnDecl) {
	if fn.Doc != "" {
		e.ln("/* %s */", fn.Doc)
	}
	attribs := ""
	if fn.HasAnnotation(AnnInline) {
		attribs = "__attribute__((always_inline)) inline "
	} else if fn.HasAnnotation(AnnCold) {
		attribs = "__attribute__((cold)) "
	} else if fn.HasAnnotation(AnnHot) {
		attribs = "__attribute__((hot)) "
	}
	var sig string
	if fn.Name == "main" {
		sig = "int main(int argc, char **argv)"
	} else {
		cname := fn.Name
		if fn.CName != "" {
			cname = fn.CName
		}
		sig = fmt.Sprintf("%s%s %s(%s)",
			attribs, cType(fn.RetType), safeFnName(cname), buildParamStr(fn.Params, fn.Variadic))
	}
	e.ln("%s {", sig)
	e.indent++
	e.emitBlockStmts(fn.Body)
	if fn.Name == "main" {
		e.ln("return 0;")
	}
	e.indent--
	e.ln("}")
}

func (e *Emitter) emitMethodFull(m *MethodDecl) {
	e.ln("%s %s(%s) {", cType(m.RetType), m.CName(), methodParamStr(m))
	e.indent++
	e.emitBlockStmts(m.Body)
	e.indent--
	e.ln("}")
}

func (e *Emitter) emitBlockStmts(b *Block) {
	if b == nil {
		return
	}
	// collect defers first
	var defers []Node
	for _, s := range b.Stmts {
		if ds, ok := s.(*DeferStmt); ok {
			defers = append(defers, ds.Call)
		}
	}
	for _, s := range b.Stmts {
		if _, ok := s.(*DeferStmt); !ok {
			e.emitStmt(s)
		}
	}
	// emit deferred calls in LIFO order
	for i := len(defers) - 1; i >= 0; i-- {
		e.ln("/* deferred */ %s;", e.emitExpr(defers[i]))
	}
}

// ─────────────────────────────────────────────────────────────────────────────
//  Statement emission
// ─────────────────────────────────────────────────────────────────────────────

func (e *Emitter) emitStmt(n Node) {
	if n == nil {
		return
	}
	switch s := n.(type) {
	case *VarDecl:
		if !s.IsGlobal {
			e.emitVarDecl(s)
		}
	case *ReturnStmt:
		e.emitReturn(s)
	case *IfStmt:
		e.emitIf(s)
	case *UnlessStmt:
		e.ln("if (!(%s)) {", e.emitExpr(s.Cond))
		e.indent++
		for _, st := range s.Body.Stmts {
			e.emitStmt(st)
		}
		e.indent--
		if s.Else != nil {
			e.ln("} else {")
			e.indent++
			for _, st := range s.Else.Stmts {
				e.emitStmt(st)
			}
			e.indent--
		}
		e.ln("}")
	case *WhileStmt:
		e.ln("while (%s) {", e.emitExpr(s.Cond))
		e.indent++
		for _, st := range s.Body.Stmts {
			e.emitStmt(st)
		}
		e.indent--
		e.ln("}")
	case *UntilStmt:
		e.ln("while (!(%s)) {", e.emitExpr(s.Cond))
		e.indent++
		for _, st := range s.Body.Stmts {
			e.emitStmt(st)
		}
		e.indent--
		e.ln("}")
	case *ForRangeStmt:
		e.emitForRange(s)
	case *MatchStmt:
		e.emitMatch(s)
	case *TryCatchStmt:
		e.emitTryCatch(s)
	case *AssignStmt:
		e.ln("%s %s %s;", e.emitExpr(s.LHS), s.Op, e.emitExpr(s.Value))
	// ── NEW: mod property set statement ──────────────────────────────────────
	case *ModPropSetStmt:
		e.emitModPropSet(s)
	case *ExprStmt:
		ex := e.emitExpr(s.Expr)
		if ex != "" && ex != "0" {
			e.ln("%s;", ex)
		}
	case *PrintStmt:
		e.emitPrint(s)
	case *ExitStmt:
		e.ln("exit((int)(%s));", e.emitExpr(s.Code))
	case *BreakStmt:
		e.ln("break;")
	case *ContinueStmt:
		e.ln("continue;")
	case *AssertStmt:
		e.ln("if (!(%s)) { fprintf(stderr, \"Assertion failed: %%s\\n\", %s); exit(1); }",
			e.emitExpr(s.Cond), e.emitExpr(s.Msg))
	case *SpawnStmt:
		e.ln("/* spawn */ system(%s);", e.emitExpr(s.Call))
	case *DeferStmt: /* handled in emitBlockStmts */
	case *FnDecl:
		e.emitFnFull(s)
	case *Block:
		e.ln("{")
		e.indent++
		e.emitBlockStmts(s)
		e.indent--
		e.ln("}")
	case *PipeExpr:
		e.ln("%s;", e.emitExpr(s))
	case *MacroCallChain:
		e.emitMacroChainStmt(s)
	case *MacroCallExpr:
		e.ln("%s;", e.emitMacroCall(s))
	case *RepeatStmt:
		iterVar := e.tmp()
		e.ln("for (long long %s = 0; %s < (long long)(%s); %s++) {",
			iterVar, iterVar, e.emitExpr(s.Count), iterVar)
		e.indent++
		e.emitBlockStmts(s.Body)
		e.indent--
		e.ln("}")
	case *WithStmt:
		exprStr := e.emitExpr(s.Expr)
		et := exprType(s.Expr)
		var ct string
		if et != nil && et.Kind != TyAny && et.Kind != TyUnknown {
			ct = cType(et)
		} else {
			ct = cTypeLiteral(s.Expr)
		}
		e.ln("{")
		e.indent++
		e.ln("%s %s = %s; /* with */", ct, s.As, exprStr)
		e.emitBlockStmts(s.Body)
		e.indent--
		e.ln("}")
	}
}

func (e *Emitter) emitVarDecl(v *VarDecl) {
	// Pick the most specific type available:
	// 1. ResolvedType from typechecker (most accurate)
	// 2. VarType from declaration
	// 3. Inferred from init expression (smart any-detection)
	// 4. Fallback any
	declared := v.ResolvedType
	if declared == nil {
		declared = v.VarType
	}

	var ct string
	if declared != nil && declared.Kind != TyAny && declared.Kind != TyUnknown {
		// Explicitly typed — use it directly.
		ct = cType(declared)
	} else if v.Init != nil {
		// Type is any/unknown — infer from the init expression.
		ct = cTypeFromExpr(declared, v.Init)
	} else {
		// No init, no concrete type — use void* as the safest any.
		ct = "void*"
	}

	prefix := ""
	if v.IsConst {
		prefix = "const "
	}

	if v.Init != nil {
		e.ln("%s%s %s = %s;", prefix, ct, v.Name, e.emitExpr(v.Init))
	} else {
		// Zero-initialize based on type.
		zero := zeroValueFor(ct)
		e.ln("%s%s %s = %s;", prefix, ct, v.Name, zero)
	}
}

func (e *Emitter) emitReturn(r *ReturnStmt) {
	if r.Value == nil {
		e.ln("return;")
	} else {
		e.ln("return %s;", e.emitExpr(r.Value))
	}
}

func (e *Emitter) emitIf(s *IfStmt) {
	e.ln("if (%s) {", e.emitExpr(s.Cond))
	e.indent++
	for _, st := range s.Then.Stmts {
		e.emitStmt(st)
	}
	e.indent--
	for _, el := range s.Elifs {
		e.ln("} else if (%s) {", e.emitExpr(el.Cond))
		e.indent++
		for _, st := range el.Body.Stmts {
			e.emitStmt(st)
		}
		e.indent--
	}
	if s.Else != nil {
		e.ln("} else {")
		e.indent++
		for _, st := range s.Else.Stmts {
			e.emitStmt(st)
		}
		e.indent--
	}
	e.ln("}")
}

func (e *Emitter) emitForRange(s *ForRangeStmt) {
	from := e.emitExpr(s.From)
	to := e.emitExpr(s.To)
	step := "1"
	if s.Step != nil {
		step = e.emitExpr(s.Step)
	}
	if s.Reverse {
		e.ln("for (long long %s = (%s)-1; %s >= %s; %s -= %s) {",
			s.Var, to, s.Var, from, s.Var, step)
	} else {
		e.ln("for (long long %s = %s; %s < %s; %s += %s) {",
			s.Var, from, s.Var, to, s.Var, step)
	}
	e.indent++
	for _, st := range s.Body.Stmts {
		e.emitStmt(st)
	}
	e.indent--
	e.ln("}")
}

func (e *Emitter) emitMatch(s *MatchStmt) {
	expr := e.emitExpr(s.Expr)
	tmp := e.tmp()
	e.ln("{ long long %s = (long long)(%s);", tmp, expr)
	e.indent++
	first := true
	for _, arm := range s.Arms {
		if arm.IsWild {
			if !first {
				e.sb.WriteString("} else ")
			} else {
				e.sb.WriteString(strings.Repeat("    ", e.indent))
			}
			e.sb.WriteString("{\n")
			e.indent++
			for _, st := range arm.Body.Stmts {
				e.emitStmt(st)
			}
			e.indent--
			e.sb.WriteString(strings.Repeat("    ", e.indent) + "}\n")
		} else {
			kw := "if"
			if !first {
				kw = "} else if"
			}
			var cond string
			if len(arm.Patterns) > 0 {
				var parts []string
				for _, pat := range arm.Patterns {
					parts = append(parts, fmt.Sprintf("(%s == (long long)(%s))", tmp, e.emitExpr(pat)))
				}
				cond = strings.Join(parts, " || ")
			} else {
				cond = fmt.Sprintf("(%s == (long long)(%s))", tmp, e.emitExpr(arm.Pattern))
			}
			if arm.Guard != nil {
				cond += " && (" + e.emitExpr(arm.Guard) + ")"
			}
			e.ln("%s (%s) {", kw, cond)
			e.indent++
			for _, st := range arm.Body.Stmts {
				e.emitStmt(st)
			}
			e.indent--
		}
		first = false
	}
	e.indent--
	e.ln("}")
}

func (e *Emitter) emitTryCatch(s *TryCatchStmt) {
	e.ln("{ /* try */")
	e.indent++
	e.ln("errno = 0;")
	if s.Try != nil {
		for _, st := range s.Try.Stmts {
			e.emitStmt(st)
		}
	}
	e.indent--
	if s.Catch != nil {
		e.ln("if (errno != 0) { /* catch */")
		e.indent++
		if s.ErrVar != "" {
			e.ln("int %s = errno;", s.ErrVar)
		}
		for _, st := range s.Catch.Stmts {
			e.emitStmt(st)
		}
		e.indent--
		e.ln("}")
	}
	if s.Finally != nil {
		e.ln("{ /* finally */")
		e.indent++
		for _, st := range s.Finally.Stmts {
			e.emitStmt(st)
		}
		e.indent--
		e.ln("}")
	}
	e.ln("}")
}

func (e *Emitter) emitPrint(s *PrintStmt) {
	dest := "stdout"
	if s.ToStderr {
		dest = "stderr"
	}
	if len(s.Args) == 0 {
		if s.Newline {
			e.ln("fprintf(%s, \"\\n\");", dest)
		}
		return
	}

	// For each argument, emit a separate fprintf so we can handle
	// struct/pointer types with multi-field expansion cleanly.
	for i, a := range s.Args {
		t := exprType(a)
		expr := e.emitExpr(a)

		// Add space separator between args (matches original " " join).
		if i > 0 {
			e.ln("fprintf(%s, \" \");", dest)
		}

		// Struct pointer: dereference and print each field.
		if t != nil && t.Kind == TyRef && t.Elem != nil && t.Elem.Kind == TyStruct {
			sd := e.findStruct(t.Elem.Name)
			if sd != nil && len(sd.Fields) > 0 {
				e.emitPrintStructPtr(dest, expr, sd)
				continue
			}
			// Unknown struct → print pointer address
			e.ln("fprintf(%s, \"%%p\", (void*)(%s));", dest, expr)
			continue
		}

		// Struct value: print each field.
		if t != nil && t.Kind == TyStruct {
			sd := e.findStruct(t.Name)
			if sd != nil && len(sd.Fields) > 0 {
				e.emitPrintStructVal(dest, expr, sd)
				continue
			}
			e.ln("fprintf(%s, \"<struct %s>\");", dest, t.Name)
			continue
		}

		// any/unknown: detect real type from expression structure.
		if t == nil || t.Kind == TyAny || t.Kind == TyUnknown {
			litType := cTypeLiteral(a)
			switch litType {
			case "const char*":
				e.ln("fprintf(%s, \"%%s\", %s);", dest, expr)
			case "double":
				e.ln("fprintf(%s, \"%%g\", (double)(%s));", dest, expr)
			default:
				// long long — use __zx_any_str to print nicely
				e.ln("fprintf(%s, \"%%s\", __zx_any_str((long long)(%s)));", dest, expr)
			}
			continue
		}

		// Normal typed value
		e.ln("fprintf(%s, \"%s\", %s);", dest, fmtFor(t), expr)
	}

	if s.Newline {
		e.ln("fprintf(%s, \"\\n\");", dest)
	}
}

// emitPrintStructPtr prints the fields of a struct accessed via pointer.
func (e *Emitter) emitPrintStructPtr(dest, ptrExpr string, sd *StructDecl) {
	e.ln("fprintf(%s, \"%s{\");", dest, sd.Name)
	for i, f := range sd.Fields {
		ft := f.Type
		if ft == nil {
			ft = TypAny
		}
		var fieldExpr string
		if ft.Kind == TyAny || ft.Kind == TyUnknown {
			fieldExpr = fmt.Sprintf("__zx_any_str((%s)->%s)", ptrExpr, f.Name)
			e.ln("fprintf(%s, \"%s: %%s\", %s);", dest, f.Name, fieldExpr)
		} else {
			fieldExpr = fmt.Sprintf("(%s)->%s", ptrExpr, f.Name)
			e.ln("fprintf(%s, \"%s: %s\", %s);", dest, f.Name, fmtFor(ft), fieldExpr)
		}
		if i < len(sd.Fields)-1 {
			e.ln("fprintf(%s, \", \");", dest)
		}
	}
	e.ln("fprintf(%s, \"}\");", dest)
}

// emitPrintStructVal prints the fields of a struct value (not pointer).
func (e *Emitter) emitPrintStructVal(dest, valExpr string, sd *StructDecl) {
	e.ln("fprintf(%s, \"%s{\");", dest, sd.Name)
	for i, f := range sd.Fields {
		ft := f.Type
		if ft == nil {
			ft = TypAny
		}
		var fieldExpr string
		if ft.Kind == TyAny || ft.Kind == TyUnknown {
			fieldExpr = fmt.Sprintf("__zx_any_str((%s).%s)", valExpr, f.Name)
			e.ln("fprintf(%s, \"%s: %%s\", %s);", dest, f.Name, fieldExpr)
		} else {
			fieldExpr = fmt.Sprintf("(%s).%s", valExpr, f.Name)
			e.ln("fprintf(%s, \"%s: %s\", %s);", dest, f.Name, fmtFor(ft), fieldExpr)
		}
		if i < len(sd.Fields)-1 {
			e.ln("fprintf(%s, \", \");", dest)
		}
	}
	e.ln("fprintf(%s, \"}\");", dest)
}

// ─────────────────────────────────────────────────────────────────────────────
//  Expression emission
// ─────────────────────────────────────────────────────────────────────────────

func (e *Emitter) emitExpr(n Node) string {
	if n == nil {
		return "0"
	}
	switch ex := n.(type) {
	case *IntLit:
		return fmt.Sprintf("%dLL", ex.Val)
	case *FloatLit:
		s := fmt.Sprintf("%g", ex.Val)
		if !strings.Contains(s, ".") && !strings.Contains(s, "e") {
			s += ".0"
		}
		return s
	case *BoolLit:
		if ex.Val {
			return "1"
		}
		return "0"
	case *StrLit:
		return cEscapeString(ex.Val)
	case *NilLit:
		return "NULL"
	case *Ident:
		return safeFnName(ex.Name)

	// ── NEW: mod property read ────────────────────────────────────────────────
	case *ModPropGetExpr:
		return e.emitModPropGet(ex)

	case *BinExpr:
		return fmt.Sprintf("(%s %s %s)", e.emitExpr(ex.LHS), ex.Op, e.emitExpr(ex.RHS))
	case *UnaryExpr:
		return fmt.Sprintf("(%s%s)", ex.Op, e.emitExpr(ex.Operand))
	case *TernaryExpr:
		return fmt.Sprintf("((%s) ? (%s) : (%s))",
			e.emitExpr(ex.Cond), e.emitExpr(ex.Then), e.emitExpr(ex.Else))

	case *CallExpr:
		fn := e.emitExpr(ex.Func)
		var argStrs []string
		for _, a := range ex.Args {
			argStrs = append(argStrs, e.emitExpr(a))
		}
		return fmt.Sprintf("%s(%s)", fn, strings.Join(argStrs, ", "))

	case *BuiltinExpr:
		return e.emitBuiltin(ex)

	case *ModCallExpr:
		var argStrs []string
		for _, a := range ex.Args {
			argStrs = append(argStrs, e.emitExpr(a))
		}
		return fmt.Sprintf("%s(%s)", modCFnName(ex.Mod, ex.Fn), strings.Join(argStrs, ", "))

	case *MethodCallExpr:
		// When receiver has unknown type it is a mod name (nested paths supported).
		if id, ok := ex.Recv.(*Ident); ok && (id.Typ == nil || id.Typ.Kind == TyUnknown) {
			var argStrs []string
			for _, a := range ex.Args {
				argStrs = append(argStrs, e.emitExpr(a))
			}
			return fmt.Sprintf("%s(%s)", modCFnName(id.Name, ex.Method), strings.Join(argStrs, ", "))
		}
		recv := e.emitExpr(ex.Recv)
		recvT := exprType(ex.Recv)
		structName := ""
		if recvT != nil {
			if recvT.Kind == TyStruct {
				structName = recvT.Name
			}
			if recvT.Kind == TyRef && recvT.Elem != nil && recvT.Elem.Kind == TyStruct {
				structName = recvT.Elem.Name
			}
		}
		var argStrs []string
		argStrs = append(argStrs, recv)
		for _, a := range ex.Args {
			argStrs = append(argStrs, e.emitExpr(a))
		}
		if structName != "" {
			return fmt.Sprintf("%s_%s(%s)", structName, ex.Method, strings.Join(argStrs, ", "))
		}
		return fmt.Sprintf("%s(%s)", ex.Method, strings.Join(argStrs, ", "))

	case *PipeExpr:
		if len(ex.Steps) == 0 {
			return "0"
		}
		result := e.emitExpr(ex.Steps[0])
		for _, step := range ex.Steps[1:] {
			result = e.emitPipeStep(step, result)
		}
		return result

	case *IndexExpr:
		return fmt.Sprintf("%s[%s]", e.emitExpr(ex.Obj), e.emitExpr(ex.Idx))

	case *FieldExpr:
		obj := e.emitExpr(ex.Obj)
		objT := exprType(ex.Obj)
		if objT != nil && objT.Kind == TyRef {
			return fmt.Sprintf("%s->%s", obj, ex.Field)
		}
		return fmt.Sprintf("%s.%s", obj, ex.Field)

	case *CastExpr:
		return fmt.Sprintf("((%s)(%s))", cType(ex.ToType), e.emitExpr(ex.Operand))

	case *AddrExpr:
		if ex.Deref {
			return fmt.Sprintf("(*%s)", e.emitExpr(ex.Operand))
		}
		return fmt.Sprintf("(&%s)", e.emitExpr(ex.Operand))

	case *StructInit:
		// Find the struct declaration so we know each field's declared type.
		// This lets us cast values correctly when fields are void* (any-typed).
		sd := e.findStruct(ex.Name)
		var parts []string
		for _, fi := range ex.Fields {
			val := e.emitExpr(fi.Value)
			// If field is any-typed (void* in C), cast the value to void*.
			// This avoids GCC warnings about pointer/integer mismatches.
			if sd != nil {
				fieldType := e.structFieldType(sd, fi.Name)
				if fieldType == nil || fieldType.Kind == TyAny || fieldType.Kind == TyUnknown {
					// Determine the actual C type of the value for a correct cast.
					valCType := cTypeLiteral(fi.Value)
					if valCType == "const char*" {
						val = fmt.Sprintf("(void*)(%s)", val)
					} else if valCType != "long long" && valCType != "int" {
						val = fmt.Sprintf("(void*)(uintptr_t)(%s)", val)
					} else {
						val = fmt.Sprintf("(void*)(uintptr_t)(%s)", val)
					}
				}
			}
			parts = append(parts, fmt.Sprintf(".%s = %s", fi.Name, val))
		}
		if ex.HeapAlloc {
			return fmt.Sprintf("((%s*)memcpy(malloc(sizeof(%s)), &(%s){%s}, sizeof(%s)))",
				ex.Name, ex.Name, ex.Name, strings.Join(parts, ", "), ex.Name)
		}
		return fmt.Sprintf("(%s){%s}", ex.Name, strings.Join(parts, ", "))

	case *ArrayLit:
		var parts []string
		for _, el := range ex.Elems {
			parts = append(parts, e.emitExpr(el))
		}
		return fmt.Sprintf("{%s}", strings.Join(parts, ", "))

	case *SizeofExpr:
		return fmt.Sprintf("(long long)sizeof(%s)", cType(ex.Of))

	case *TemplateStr:
		return e.emitTemplateStr(ex)

	case *MultilineStr:
		return e.emitMultilineStr(ex)

	case *CmdExpr:
		if ex.CaptureOutput {
			return fmt.Sprintf("__zx_capture_cmd(%s)", e.emitExpr(ex.Command))
		}
		return fmt.Sprintf("system(%s)", e.emitExpr(ex.Command))

	case *ReadFileExpr:
		return fmt.Sprintf("__zx_read_file(%s)", e.emitExpr(ex.Path))

	case *MacroCallExpr:
		return e.emitMacroCall(ex)
	case *MacroCallChain:
		return e.emitMacroChainExpr(ex)
	case *TypeofExpr:
		return e.emitTypeof(ex)
	case *BangMacroExpr:
		return e.emitBangMacro(ex)
	case *LambdaExpr:
		return e.emitLambda(ex)
	case *MacroApplyExpr:
		return e.emitMacroApply(ex)

	default:
		return "0"
	}
}

// ─────────────────────────────────────────────────────────────────────────────
//  Lambda emission
// ─────────────────────────────────────────────────────────────────────────────

func (e *Emitter) emitLambda(lam *LambdaExpr) string {
	name := e.lambdaName()
	params := buildParamStr(lam.Params, false)
	retT := lam.RetType
	if retT == nil {
		retT = TypVoid
	}
	e.lln("static %s %s(%s) {", cType(retT), name, params)
	savedSB := e.sb
	e.sb = e.lambdaBodies
	e.indent = 1
	e.emitBlockStmts(lam.Body)
	e.lambdaBodies = e.sb
	e.sb = savedSB
	e.indent = 0
	e.lln("}")
	e.lln("")
	return name
}

func (e *Emitter) emitMacroApply(ma *MacroApplyExpr) string {
	valStr := e.emitExpr(ma.Value)
	var argStrs []string
	argStrs = append(argStrs, valStr)
	for _, a := range ma.Args {
		argStrs = append(argStrs, e.emitExpr(a))
	}
	return fmt.Sprintf("%s(%s)", macroFnName(ma.Macro), strings.Join(argStrs, ", "))
}

// ─────────────────────────────────────────────────────────────────────────────
//  Builtin expression emission
// ─────────────────────────────────────────────────────────────────────────────

func (e *Emitter) emitBuiltin(b *BuiltinExpr) string {
	switch b.Name {
	case "len":
		if len(b.Args) == 1 {
			t := exprType(b.Args[0])
			arg := e.emitExpr(b.Args[0])
			// If arg is str/char* → strlen directly.
			// If arg is any/unknown → use safe __zx_len to avoid segfault.
			if t != nil && (t.Kind == TyStr || (t.Kind == TyRef && t.Elem != nil && t.Elem.Kind == TyChar)) {
				return fmt.Sprintf("(long long)strlen((const char*)(%s))", arg)
			}
			if t == nil || t.Kind == TyAny || t.Kind == TyUnknown {
				return fmt.Sprintf("__zx_len((long long)(%s))", arg)
			}
			return fmt.Sprintf("(long long)strlen((const char*)(%s))", arg)
		}
	case "input":
		prompt := "NULL"
		if len(b.Args) > 0 {
			prompt = e.emitExpr(b.Args[0])
		}
		return fmt.Sprintf("__zx_input(%s)", prompt)
	case "alloc":
		if len(b.Args) == 1 {
			return fmt.Sprintf("malloc((size_t)(%s))", e.emitExpr(b.Args[0]))
		}
	case "zalloc":
		if len(b.Args) == 2 {
			return fmt.Sprintf("calloc((size_t)(%s), (size_t)(%s))",
				e.emitExpr(b.Args[0]), e.emitExpr(b.Args[1]))
		}
	case "free":
		if len(b.Args) == 1 {
			return fmt.Sprintf("free(%s)", e.emitExpr(b.Args[0]))
		}
	case "system", "getenv":
		if len(b.Args) == 1 {
			return fmt.Sprintf("%s(%s)", b.Name, e.emitExpr(b.Args[0]))
		}
	case "str_eq":
		if len(b.Args) == 2 {
			return fmt.Sprintf("(strcmp(%s, %s) == 0)", e.emitExpr(b.Args[0]), e.emitExpr(b.Args[1]))
		}
	case "str_ne":
		if len(b.Args) == 2 {
			return fmt.Sprintf("(strcmp(%s, %s) != 0)", e.emitExpr(b.Args[0]), e.emitExpr(b.Args[1]))
		}
	case "is_nil":
		if len(b.Args) == 1 {
			return fmt.Sprintf("((void*)(%s) == NULL)", e.emitExpr(b.Args[0]))
		}
	case "not_nil":
		if len(b.Args) == 1 {
			return fmt.Sprintf("((void*)(%s) != NULL)", e.emitExpr(b.Args[0]))
		}
	case "to_int":
		if len(b.Args) == 1 {
			return fmt.Sprintf("((long long)(%s))", e.emitExpr(b.Args[0]))
		}
	case "to_float":
		if len(b.Args) == 1 {
			return fmt.Sprintf("((double)(%s))", e.emitExpr(b.Args[0]))
		}
	case "to_bool":
		if len(b.Args) == 1 {
			return fmt.Sprintf("(!!((%s)))", e.emitExpr(b.Args[0]))
		}
	case "to_char":
		if len(b.Args) == 1 {
			return fmt.Sprintf("((char)(%s))", e.emitExpr(b.Args[0]))
		}
	case "abs":
		if len(b.Args) == 1 {
			a := e.emitExpr(b.Args[0])
			return fmt.Sprintf("(((%s) < 0) ? -(%s) : (%s))", a, a, a)
		}
	case "min":
		if len(b.Args) == 2 {
			a, bv := e.emitExpr(b.Args[0]), e.emitExpr(b.Args[1])
			return fmt.Sprintf("((%s) < (%s) ? (%s) : (%s))", a, bv, a, bv)
		}
	case "max":
		if len(b.Args) == 2 {
			a, bv := e.emitExpr(b.Args[0]), e.emitExpr(b.Args[1])
			return fmt.Sprintf("((%s) > (%s) ? (%s) : (%s))", a, bv, a, bv)
		}
	case "clamp":
		if len(b.Args) == 3 {
			v, lo, hi := e.emitExpr(b.Args[0]), e.emitExpr(b.Args[1]), e.emitExpr(b.Args[2])
			return fmt.Sprintf("((%s) < (%s) ? (%s) : ((%s) > (%s) ? (%s) : (%s)))",
				v, lo, lo, v, hi, hi, v)
		}
	case "sizeof":
		if len(b.Args) == 1 {
			return fmt.Sprintf("(long long)sizeof(%s)", e.emitExpr(b.Args[0]))
		}
	}
	var argStrs []string
	for _, a := range b.Args {
		argStrs = append(argStrs, e.emitExpr(a))
	}
	return fmt.Sprintf("%s(%s)", b.Name, strings.Join(argStrs, ", "))
}

