package main

import (
	"fmt"
	"strings"
)

// ─────────────────────────────────────────────────────────────────────────────
//  C Code Emitter
// ─────────────────────────────────────────────────────────────────────────────

type Emitter struct {
	sb      strings.Builder
	indent  int
	tempIdx int
}

func Emit(prog *Program) string {
	e := &Emitter{}
	e.emitProgram(prog)
	return e.sb.String()
}

func (e *Emitter) write(s string) { e.sb.WriteString(s) }
func (e *Emitter) writeln(s string) {
	e.sb.WriteString(strings.Repeat("    ", e.indent))
	e.sb.WriteString(s)
	e.sb.WriteByte('\n')
}

func (e *Emitter) line(f string, args ...any) {
	e.writeln(fmt.Sprintf(f, args...))
}

func (e *Emitter) emitProgram(prog *Program) {
	e.writeln("/* generated by zxc — do not edit */")
	e.writeln("#include <stdio.h>")
	e.writeln("#include <stdlib.h>")
	e.writeln("#include <string.h>")
	e.writeln("#include <math.h>")
	e.writeln("#include <stdbool.h>")
	e.writeln("")

	// user imports
	for _, imp := range prog.Imports {
		e.line("#include <%s>", imp.Path)
	}
	if len(prog.Imports) > 0 {
		e.writeln("")
	}

	// struct declarations
	for _, s := range prog.Structs {
		e.emitStruct(s)
	}

	// extern function prototypes
	for _, ext := range prog.Externs {
		e.emitExternProto(ext)
	}
	if len(prog.Externs) > 0 {
		e.writeln("")
	}

	// forward-declare user functions
	for _, stmt := range prog.TopStmts {
		if fn, ok := stmt.(*FnDecl); ok {
			e.emitFnProto(fn)
		}
	}
	e.writeln("")

	// collect top-level non-fn stmts → goes into main()
	var mainStmts []Node
	var fnDecls []*FnDecl
	for _, stmt := range prog.TopStmts {
		if fn, ok := stmt.(*FnDecl); ok {
			fnDecls = append(fnDecls, fn)
		} else {
			mainStmts = append(mainStmts, stmt)
		}
	}

	// emit function bodies
	for _, fn := range fnDecls {
		if fn.Name == "main" {
			// user defined main — emit as-is but wrapped
			e.emitFnBody(fn)
		} else {
			e.emitFnBody(fn)
		}
		e.writeln("")
	}

	// emit main() if there are top-level stmts and no user main
	hasUserMain := false
	for _, fn := range fnDecls {
		if fn.Name == "main" {
			hasUserMain = true
		}
	}
	if len(mainStmts) > 0 && !hasUserMain {
		e.writeln("int main(int argc, char **argv) {")
		e.indent++
		for _, s := range mainStmts {
			e.emitStmt(s)
		}
		e.writeln("return 0;")
		e.indent--
		e.writeln("}")
	} else if !hasUserMain && len(mainStmts) == 0 {
		e.writeln("int main(int argc, char **argv) { return 0; }")
	}
}

// ── Types ─────────────────────────────────────────────────────────────────────

func cType(t *ZXType) string {
	if t == nil {
		return "void"
	}
	switch t.Kind {
	case TyInt:
		return "long long"
	case TyFloat:
		return "double"
	case TyBool:
		return "int"
	case TyStr:
		return "const char*"
	case TyChar:
		return "char"
	case TyVoid:
		return "void"
	case TyPtr:
		if t.PtrElem != nil {
			return cType(t.PtrElem) + "*"
		}
		return "void*"
	case TyStruct:
		return t.Name
	default:
		return "void"
	}
}

// ── Struct ────────────────────────────────────────────────────────────────────

func (e *Emitter) emitStruct(s *StructDecl) {
	e.line("typedef struct %s {", s.Name)
	e.indent++
	for _, f := range s.Fields {
		e.line("%s %s;", cType(f.Type), f.Name)
	}
	e.indent--
	e.line("} %s;", s.Name)
	e.writeln("")
}

// ── Extern proto ──────────────────────────────────────────────────────────────

func (e *Emitter) emitExternProto(ext *ExternDecl) {
	params := buildParamStr(ext.Params, ext.Variadic)
	e.line("extern %s %s(%s);", cType(ext.RetType), ext.Name, params)
}

// ── Function proto ────────────────────────────────────────────────────────────

func (e *Emitter) emitFnProto(fn *FnDecl) {
	if fn.Name == "main" {
		e.writeln("int main(int argc, char **argv);")
		return
	}
	params := buildParamStr(fn.Params, fn.Variadic)
	e.line("%s %s(%s);", cType(fn.RetType), fn.Name, params)
}

func buildParamStr(params []Param, variadic bool) string {
	if len(params) == 0 {
		if variadic {
			return "..."
		}
		return "void"
	}
	parts := make([]string, len(params))
	for i, p := range params {
		parts[i] = fmt.Sprintf("%s %s", cType(p.Type), p.Name)
	}
	if variadic {
		parts = append(parts, "...")
	}
	return strings.Join(parts, ", ")
}

// ── Function body ─────────────────────────────────────────────────────────────

func (e *Emitter) emitFnBody(fn *FnDecl) {
	if fn.Name == "main" {
		e.writeln("int main(int argc, char **argv) {")
	} else {
		params := buildParamStr(fn.Params, fn.Variadic)
		e.line("%s %s(%s) {", cType(fn.RetType), fn.Name, params)
	}
	e.indent++
	for _, s := range fn.Body.Stmts {
		e.emitStmt(s)
	}
	if fn.Name == "main" {
		e.writeln("return 0;")
	}
	e.indent--
	e.writeln("}")
}

// ── Statements ────────────────────────────────────────────────────────────────

func (e *Emitter) emitStmt(n Node) {
	if n == nil {
		return
	}
	switch s := n.(type) {
	case *VarDecl:
		e.emitVarDecl(s)
	case *ReturnStmt:
		e.emitReturn(s)
	case *IfStmt:
		e.emitIf(s)
	case *WhileStmt:
		e.emitWhile(s)
	case *ForRangeStmt:
		e.emitForRange(s)
	case *AssignStmt:
		e.emitAssign(s)
	case *ExprStmt:
		expr := e.emitExpr(s.Expr)
		e.line("%s;", expr)
	case *PrintStmt:
		e.emitPrint(s)
	case *ExitStmt:
		code := e.emitExpr(s.Code)
		e.line("exit((int)(%s));", code)
	case *BreakStmt:
		e.writeln("break;")
	case *ContinueStmt:
		e.writeln("continue;")
	case *FnDecl:
		// nested fn — emit after current scope (not truly nested in C, just emit)
		e.emitFnBody(s)
	case *Block:
		e.writeln("{")
		e.indent++
		for _, st := range s.Stmts {
			e.emitStmt(st)
		}
		e.indent--
		e.writeln("}")
	}
}

func (e *Emitter) emitVarDecl(v *VarDecl) {
	typ := v.ResolvedType
	if typ == nil {
		typ = v.VarType
	}
	if typ == nil {
		typ = TypInt
	}
	prefix := ""
	if v.IsConst {
		prefix = "const "
	}
	if v.Init != nil {
		init := e.emitExpr(v.Init)
		e.line("%s%s %s = %s;", prefix, cType(typ), v.Name, init)
	} else {
		e.line("%s%s %s;", prefix, cType(typ), v.Name)
	}
}

func (e *Emitter) emitReturn(r *ReturnStmt) {
	if r.Value == nil {
		e.writeln("return;")
	} else {
		val := e.emitExpr(r.Value)
		e.line("return %s;", val)
	}
}

func (e *Emitter) emitIf(s *IfStmt) {
	cond := e.emitExpr(s.Cond)
	e.line("if (%s) {", cond)
	e.indent++
	for _, st := range s.Then.Stmts {
		e.emitStmt(st)
	}
	e.indent--
	for _, el := range s.Elifs {
		ec := e.emitExpr(el.Cond)
		e.line("} else if (%s) {", ec)
		e.indent++
		for _, st := range el.Body.Stmts {
			e.emitStmt(st)
		}
		e.indent--
	}
	if s.Else != nil {
		e.writeln("} else {")
		e.indent++
		for _, st := range s.Else.Stmts {
			e.emitStmt(st)
		}
		e.indent--
	}
	e.writeln("}")
}

func (e *Emitter) emitWhile(s *WhileStmt) {
	cond := e.emitExpr(s.Cond)
	e.line("while (%s) {", cond)
	e.indent++
	for _, st := range s.Body.Stmts {
		e.emitStmt(st)
	}
	e.indent--
	e.writeln("}")
}

func (e *Emitter) emitForRange(s *ForRangeStmt) {
	from := e.emitExpr(s.From)
	to := e.emitExpr(s.To)
	e.line("for (long long %s = %s; %s < %s; %s++) {", s.Var, from, s.Var, to, s.Var)
	e.indent++
	for _, st := range s.Body.Stmts {
		e.emitStmt(st)
	}
	e.indent--
	e.writeln("}")
}

func (e *Emitter) emitAssign(s *AssignStmt) {
	lhs := e.emitExpr(s.LHS)
	rhs := e.emitExpr(s.Value)
	e.line("%s %s %s;", lhs, s.Op, rhs)
}

func (e *Emitter) emitPrint(s *PrintStmt) {
	if len(s.Args) == 0 {
		if s.Newline {
			e.writeln(`printf("\n");`)
		}
		return
	}

	// build format string + args
	var fmtParts []string
	var args []string

	for _, a := range s.Args {
		typ := exprType(a)
		fmtParts = append(fmtParts, fmtFor(typ))
		args = append(args, e.emitExpr(a))
	}

	if s.Newline {
		fmtParts = append(fmtParts, "\\n")
	}

	fmtStr := strings.Join(fmtParts, " ")
	if len(args) > 0 {
		e.line(`printf("%s", %s);`, fmtStr, strings.Join(args, ", "))
	} else {
		e.line(`printf("%s");`, fmtStr)
	}
}

func fmtFor(t *ZXType) string {
	if t == nil {
		return "%s"
	}
	switch t.Kind {
	case TyInt:
		return "%lld"
	case TyFloat:
		return "%g"
	case TyBool:
		return "%d"
	case TyStr:
		return "%s"
	case TyChar:
		return "%c"
	case TyPtr:
		return "%p"
	default:
		return "%s"
	}
}

// best-effort type for print format
func exprType(n Node) *ZXType {
	switch e := n.(type) {
	case *IntLit:
		return TypInt
	case *FloatLit:
		return TypFloat
	case *BoolLit:
		return TypBool
	case *StrLit:
		return TypStr
	case *Ident:
		return e.Typ
	case *BinExpr:
		return e.Typ
	case *UnaryExpr:
		return e.Typ
	case *CallExpr:
		return e.Typ
	case *CastExpr:
		return e.Typ
	case *AddrExpr:
		return e.Typ
	case *FieldExpr:
		return e.Typ
	default:
		return TypUnknown
	}
}

// ── Expressions ───────────────────────────────────────────────────────────────

func (e *Emitter) emitExpr(n Node) string {
	if n == nil {
		return "0"
	}
	switch ex := n.(type) {
	case *IntLit:
		return fmt.Sprintf("%dLL", ex.Val)
	case *FloatLit:
		return fmt.Sprintf("%g", ex.Val)
	case *BoolLit:
		if ex.Val {
			return "1"
		}
		return "0"
	case *StrLit:
		return fmt.Sprintf("%q", ex.Val)
	case *NilLit:
		return "NULL"
	case *Ident:
		return ex.Name
	case *BinExpr:
		l := e.emitExpr(ex.LHS)
		r := e.emitExpr(ex.RHS)
		return fmt.Sprintf("(%s %s %s)", l, ex.Op, r)
	case *UnaryExpr:
		inner := e.emitExpr(ex.Operand)
		return fmt.Sprintf("(%s%s)", ex.Op, inner)
	case *CallExpr:
		fn := e.emitExpr(ex.Func)
		var argStrs []string
		for _, a := range ex.Args {
			argStrs = append(argStrs, e.emitExpr(a))
		}
		return fmt.Sprintf("%s(%s)", fn, strings.Join(argStrs, ", "))
	case *IndexExpr:
		obj := e.emitExpr(ex.Obj)
		idx := e.emitExpr(ex.Idx)
		return fmt.Sprintf("%s[%s]", obj, idx)
	case *FieldExpr:
		obj := e.emitExpr(ex.Obj)
		// pointer-to-struct uses ->
		objType := exprType(ex.Obj)
		if objType != nil && objType.Kind == TyPtr {
			return fmt.Sprintf("%s->%s", obj, ex.Field)
		}
		return fmt.Sprintf("%s.%s", obj, ex.Field)
	case *CastExpr:
		inner := e.emitExpr(ex.Operand)
		return fmt.Sprintf("((%s)(%s))", cType(ex.ToType), inner)
	case *AddrExpr:
		inner := e.emitExpr(ex.Operand)
		if ex.Deref {
			return fmt.Sprintf("(*%s)", inner)
		}
		return fmt.Sprintf("(&%s)", inner)
	case *StructInit:
		var parts []string
		for _, fi := range ex.Fields {
			parts = append(parts, fmt.Sprintf(".%s = %s", fi.Name, e.emitExpr(fi.Value)))
		}
		return fmt.Sprintf("(%s){%s}", ex.Name, strings.Join(parts, ", "))
	default:
		return "0"
	}
}
