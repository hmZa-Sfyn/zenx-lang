func (e *Emitter) emitTypeof(t *TypeofExpr) string {
	typ := exprType(t.Arg)
	typeName := "any"
	if typ != nil {
		typeName = typ.String()
	}
	return cEscapeString(typeName)
}

// ─────────────────────────────────────────────────────────────────────────────
//  Macro chain emission
// ─────────────────────────────────────────────────────────────────────────────

func (e *Emitter) emitMacroChainStmt(chain *MacroCallChain) {
	recvType := exprType(chain.Recv)
	if recvType == nil {
		recvType = TypAny
	}
	recvTmp := e.tmp()
	e.ln("%s %s = (%s)(%s);", cType(recvType), recvTmp, cType(recvType), e.emitExpr(chain.Recv))

	for _, step := range chain.Steps {
		e.ln("/* macro chain: %s */", step.Macro)
		mc := e.findMacro(step.Macro)
		if mc != nil {
			e.emitUserMacroChainStep(mc, step, recvTmp, recvType)
		} else {
			e.emitBuiltinChainStep(step, recvTmp)
		}
	}
}

// findStruct looks up a struct declaration by name from the program.
func (e *Emitter) findStruct(name string) *StructDecl {
	if e.prog == nil {
		return nil
	}
	for _, s := range e.prog.Structs {
		if s.Name == name {
			return s
		}
	}
	// also check mod block structs
	for _, mb := range e.prog.ModBlocks {
		if s := e.findStructInMod(mb, name); s != nil {
			return s
		}
	}
	return nil
}

func (e *Emitter) findStructInMod(mb *ModBlock, name string) *StructDecl {
	for _, s := range mb.Structs {
		if s.Name == name {
			return s
		}
	}
	for _, nested := range mb.Mods {
		if s := e.findStructInMod(nested, name); s != nil {
			return s
		}
	}
	return nil
}

// structFieldType returns the declared type of a field in a struct, or nil.
func (e *Emitter) structFieldType(sd *StructDecl, fieldName string) *ZXType {
	for _, f := range sd.Fields {
		if f.Name == fieldName {
			return f.Type
		}
	}
	return nil
}

func (e *Emitter) findMacro(name string) *MacroDecl {
	if e.prog == nil {
		return nil
	}
	for _, mc := range e.prog.Macros {
		if mc.Name == name {
			return mc
		}
		for _, alias := range mc.Aliases {
			if alias == name {
				return mc
			}
		}
	}
	return nil
}

func (e *Emitter) emitUserMacroChainStep(mc *MacroDecl, step MacroChainStep, recvTmp string, recvType *ZXType) {
	e.ln("{")
	e.indent++

	var blockParamName string
	var valueParams []Param
	for _, p := range mc.Params {
		pt := p.Type
		if pt == nil {
			pt = TypAny
		}
		if isBlockParam(p.Name, pt) {
			blockParamName = p.Name
		} else {
			valueParams = append(valueParams, p)
		}
	}

	for _, out := range mc.Outputs {
		retT := mc.RetType
		if retT == nil || retT.Kind == TyVoid {
			retT = recvType
		}
		e.ln("%s %s = (%s)(%s); /* output */", cType(retT), out, cType(retT), recvTmp)
	}

	for i, p := range valueParams {
		pt := p.Type
		if pt == nil {
			pt = TypAny
		}
		if i == 0 {
			e.ln("%s %s = (%s)(%s); /* input */", cType(pt), p.Name, cType(pt), recvTmp)
		} else {
			argIdx := i - 1
			if argIdx < len(step.Args) {
				e.ln("%s %s = (%s)(%s); /* arg %d */", cType(pt), p.Name, cType(pt),
					e.emitExpr(step.Args[argIdx]), argIdx+1)
			} else {
				e.ln("%s %s = 0; /* arg %d not provided */", cType(pt), p.Name, argIdx+1)
			}
		}
	}

	if mc.Guard != nil {
		e.ln("if (%s) {", e.emitExpr(mc.Guard))
		e.indent++
		e.emitMacroBodyWithBlockSubst(mc.Body, blockParamName, step.Body)
		e.indent--
		if step.ElseBody != nil {
			e.ln("} else {")
			e.indent++
			e.emitBlockStmts(step.ElseBody)
			e.indent--
		}
		e.ln("}")
	} else {
		e.emitMacroBodyWithBlockSubst(mc.Body, blockParamName, step.Body)
		if step.ElseBody != nil {
			e.ln("/* chain else */")
			e.ln("{")
			e.indent++
			e.emitBlockStmts(step.ElseBody)
			e.indent--
			e.ln("}")
		}
	}

	e.indent--
	e.ln("}")
}

func (e *Emitter) emitMacroBodyWithBlockSubst(body *Block, blockParam string, doBlock *Block) {
	if body == nil {
		return
	}
	for _, stmt := range body.Stmts {
		e.emitStmtWithSubst(stmt, blockParam, doBlock)
	}
}

func (e *Emitter) emitStmtWithSubst(stmt Node, bp string, doBlock *Block) {
	if stmt == nil {
		return
	}
	if bp != "" {
		if es, ok := stmt.(*ExprStmt); ok {
			if id, ok2 := es.Expr.(*Ident); ok2 && id.Name == bp {
				if doBlock != nil {
					e.emitBlockStmts(doBlock)
				}
				return
			}
			if ce, ok2 := es.Expr.(*CallExpr); ok2 {
				if id, ok3 := ce.Func.(*Ident); ok3 && id.Name == bp {
					if doBlock != nil {
						e.emitBlockStmts(doBlock)
					}
					return
				}
			}
		}
	}
	switch s := stmt.(type) {
	case *IfStmt:
		e.ln("if (%s) {", e.emitExpr(s.Cond))
		e.indent++
		e.emitBlockWithSubst(s.Then, bp, doBlock)
		e.indent--
		for _, el := range s.Elifs {
			e.ln("} else if (%s) {", e.emitExpr(el.Cond))
			e.indent++
			e.emitBlockWithSubst(el.Body, bp, doBlock)
			e.indent--
		}
		if s.Else != nil {
			e.ln("} else {")
			e.indent++
			e.emitBlockWithSubst(s.Else, bp, doBlock)
			e.indent--
		}
		e.ln("}")
	case *UnlessStmt:
		e.ln("if (!(%s)) {", e.emitExpr(s.Cond))
		e.indent++
		e.emitBlockWithSubst(s.Body, bp, doBlock)
		e.indent--
		if s.Else != nil {
			e.ln("} else {")
			e.indent++
			e.emitBlockWithSubst(s.Else, bp, doBlock)
			e.indent--
		}
		e.ln("}")
	case *WhileStmt:
		e.ln("while (%s) {", e.emitExpr(s.Cond))
		e.indent++
		e.emitBlockWithSubst(s.Body, bp, doBlock)
		e.indent--
		e.ln("}")
	case *UntilStmt:
		e.ln("while (!(%s)) {", e.emitExpr(s.Cond))
		e.indent++
		e.emitBlockWithSubst(s.Body, bp, doBlock)
		e.indent--
		e.ln("}")
	case *ForRangeStmt:
		from := e.emitExpr(s.From)
		to := e.emitExpr(s.To)
		step := "1"
		if s.Step != nil {
			step = e.emitExpr(s.Step)
		}
		e.ln("for (long long %s = %s; %s < %s; %s += %s) {",
			s.Var, from, s.Var, to, s.Var, step)
		e.indent++
		e.emitBlockWithSubst(s.Body, bp, doBlock)
		e.indent--
		e.ln("}")
	case *Block:
		e.ln("{")
		e.indent++
		e.emitBlockWithSubst(s, bp, doBlock)
		e.indent--
		e.ln("}")
	case *TryCatchStmt:
		e.ln("{ /* try */")
		e.indent++
		e.ln("errno = 0;")
		if s.Try != nil {
			e.emitBlockWithSubst(s.Try, bp, doBlock)
		}
		e.indent--
		if s.Catch != nil {
			e.ln("if (errno != 0) { /* catch */")
			e.indent++
			if s.ErrVar != "" {
				e.ln("int %s = errno;", s.ErrVar)
			}
			e.emitBlockWithSubst(s.Catch, bp, doBlock)
			e.indent--
			e.ln("}")
		}
		if s.Finally != nil {
			e.ln("{ /* finally */")
			e.indent++
			e.emitBlockWithSubst(s.Finally, bp, doBlock)
			e.indent--
			e.ln("}")
		}
		e.ln("}")
	default:
		e.emitStmt(stmt)
	}
}

func (e *Emitter) emitBlockWithSubst(b *Block, bp string, doBlock *Block) {
	if b == nil {
		return
	}
	for _, stmt := range b.Stmts {
		e.emitStmtWithSubst(stmt, bp, doBlock)
	}
}

func (e *Emitter) emitBuiltinChainStep(step MacroChainStep, recvTmp string) {
	runBlock := func() {
		if step.Body != nil {
			e.emitBlockStmts(step.Body)
		}
	}
	emitElseBranch := func() {
		if step.ElseBody != nil {
			e.ln("} else {")
			e.indent++
			e.emitBlockStmts(step.ElseBody)
			e.indent--
		}
	}

	switch step.Macro {
	case "ifTrue", "if_true", "whenTrue", "onTrue":
		e.ln("if (%s) {", recvTmp)
		e.indent++
		runBlock()
		e.indent--
		emitElseBranch()
		e.ln("}")
	case "ifFalse", "if_false", "whenFalse", "onFalse", "unless":
		e.ln("if (!(%s)) {", recvTmp)
		e.indent++
		runBlock()
		e.indent--
		emitElseBranch()
		e.ln("}")
	case "ifNil", "if_nil", "whenNil", "onNil":
		e.ln("if ((void*)(%s) == NULL) {", recvTmp)
		e.indent++
		runBlock()
		e.indent--
		emitElseBranch()
		e.ln("}")
	case "ifNotNil", "if_not_nil", "whenNotNil", "onNotNil", "ifExists":
		e.ln("if ((void*)(%s) != NULL) {", recvTmp)
		e.indent++
		runBlock()
		e.indent--
		emitElseBranch()
		e.ln("}")
	case "ifZero", "if_zero", "whenZero":
		e.ln("if ((%s) == 0) {", recvTmp)
		e.indent++
		runBlock()
		e.indent--
		emitElseBranch()
		e.ln("}")
	case "ifNotZero", "if_not_zero", "whenNotZero":
		e.ln("if ((%s) != 0) {", recvTmp)
		e.indent++
		runBlock()
		e.indent--
		emitElseBranch()
		e.ln("}")
	case "ifPositive", "if_positive":
		e.ln("if ((%s) > 0) {", recvTmp)
		e.indent++
		runBlock()
		e.indent--
		emitElseBranch()
		e.ln("}")
	case "ifNegative", "if_negative":
		e.ln("if ((%s) < 0) {", recvTmp)
		e.indent++
		runBlock()
		e.indent--
		emitElseBranch()
		e.ln("}")
	case "ifEven", "if_even":
		e.ln("if ((%s) %% 2 == 0) {", recvTmp)
		e.indent++
		runBlock()
		e.indent--
		emitElseBranch()
		e.ln("}")
	case "ifOdd", "if_odd":
		e.ln("if ((%s) %% 2 != 0) {", recvTmp)
		e.indent++
		runBlock()
		e.indent--
		emitElseBranch()
		e.ln("}")
	case "ifGt", "if_gt":
		threshold := "0"
		if len(step.Args) > 0 {
			threshold = e.emitExpr(step.Args[0])
		}
		e.ln("if ((%s) > (%s)) {", recvTmp, threshold)
		e.indent++
		runBlock()
		e.indent--
		emitElseBranch()
		e.ln("}")
	case "ifLt", "if_lt":
		threshold := "0"
		if len(step.Args) > 0 {
			threshold = e.emitExpr(step.Args[0])
		}
		e.ln("if ((%s) < (%s)) {", recvTmp, threshold)
		e.indent++
		runBlock()
		e.indent--
		emitElseBranch()
		e.ln("}")
	case "ifGte", "if_gte":
		threshold := "0"
		if len(step.Args) > 0 {
			threshold = e.emitExpr(step.Args[0])
		}
		e.ln("if ((%s) >= (%s)) {", recvTmp, threshold)
		e.indent++
		runBlock()
		e.indent--
		emitElseBranch()
		e.ln("}")
	case "ifLte", "if_lte":
		threshold := "0"
		if len(step.Args) > 0 {
			threshold = e.emitExpr(step.Args[0])
		}
		e.ln("if ((%s) <= (%s)) {", recvTmp, threshold)
		e.indent++
		runBlock()
		e.indent--
		emitElseBranch()
		e.ln("}")
	case "ifEq", "if_eq":
		val := "0"
		if len(step.Args) > 0 {
			val = e.emitExpr(step.Args[0])
		}
		e.ln("if ((%s) == (%s)) {", recvTmp, val)
		e.indent++
		runBlock()
		e.indent--
		emitElseBranch()
		e.ln("}")
	case "then", "always", "do_always", "andThen":
		e.ln("{")
		e.indent++
		runBlock()
		e.indent--
		e.ln("}")
	case "whileTrue", "while_true":
		e.ln("while (%s) {", recvTmp)
		e.indent++
		runBlock()
		e.indent--
		e.ln("}")
	case "repeat", "times":
		i := e.tmp()
		e.ln("for (long long %s = 0; %s < (long long)(%s); %s++) {", i, i, recvTmp, i)
		e.indent++
		runBlock()
		e.indent--
		e.ln("}")
	case "map":
		if len(step.Args) > 0 {
			fn := e.emitExpr(step.Args[0])
			e.ln("%s = %s(%s); /* map */", recvTmp, fn, recvTmp)
		}
	case "tap":
		e.ln("{ /* tap */")
		e.indent++
		runBlock()
		e.indent--
		e.ln("}")
	case "printVal", "print_val", "dump":
		e.ln("printf(\"%%lld\\n\", (long long)(%s));", recvTmp)
	case "orDefault", "or_default":
		if len(step.Args) > 0 {
			def := e.emitExpr(step.Args[0])
			e.ln("if (!(%s)) { %s = (%s); }", recvTmp, recvTmp, def)
		}
	case "clampTo", "clamp_to":
		if len(step.Args) >= 2 {
			lo := e.emitExpr(step.Args[0])
			hi := e.emitExpr(step.Args[1])
			e.ln("if (%s < (%s)) %s = (%s); else if (%s > (%s)) %s = (%s);",
				recvTmp, lo, recvTmp, lo, recvTmp, hi, recvTmp, hi)
		}
	default:
		e.ln("{")
		e.indent++
		e.ln("%s = %s(%s);", recvTmp, macroFnName(step.Macro), recvTmp)
		runBlock()
		e.indent--
		e.ln("}")
	}
}

func (e *Emitter) emitMacroChainExpr(chain *MacroCallChain) string {
	e.emitMacroChainStmt(chain)
	return "0"
}

